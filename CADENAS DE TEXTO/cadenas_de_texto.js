//Cadenas de texto

//Con comillas simples
let texto1 = 'JavaScript';
document.writeln(texto1);
document.writeln('<br>');

//Con comillas dobles
let texto2 = "JavaScript";
document.writeln(texto2);
document.writeln('<br>');

//Con comillas invertidas
let texto3 = `JavaScript`;
document.writeln(texto3);
document.writeln('<br>');

//resaltar texto con comillas dobles
let texto4 = 'Eso es la "verdad" de todo';
document.writeln(texto4);
document.writeln('<br>');

//resaltar texto con comillas simples
let texto5 = "Eso es la 'verdad' de todo";
document.writeln(texto5);
document.writeln('<br>');

//Salto de línea concatenando en codigo
let texto6 = 'Esta es una cadena muy larga ' +
'que nececita que se divida en varias lineas ' +
'de lo contrario el codigo no funcionará';
document.writeln(texto6);
document.writeln('<br>');
document.writeln('<br>');

//salto de linea con \ siendo mas util en codigo
let texto7 = 'Esta es una cadena muy larga \
que nececita que se divida en varias lineas \
de lo contrario el codigo no funcionará';
document.writeln(texto7);
document.writeln('<br>');
document.writeln('<br>');

//Con salto de linea que se  muetre en pantalla con br
let texto8 = 'Esta es una cadena muy larga ' + '<br>' +
'que nececita que se divida en varias lineas ' + '<br>' +
'de lo contrario el codigo no funcionará';
document.writeln(texto8);
document.writeln('<br>');
document.writeln('<br>');




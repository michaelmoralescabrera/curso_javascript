//Operadores de comparación

let valor1= 20;
let valor2=10;

document.writeln(valor1 > valor2); //true
document.writeln('<br>'); // Salto de línea
document.writeln(valor1 < valor2); //false
document.writeln('<br>'); // Salto de línea
document.writeln(valor1 == valor2); //false // Igualdad
document.writeln('<br>'); // Salto de línea
document.writeln(valor1 != valor2); //true // Diferente
document.writeln('<br>'); // Salto de línea
document.writeln(valor1 >= valor2); //true // Mayor o igual
document.writeln('<br>'); // Salto de línea
document.writeln(valor1 <= valor2); //false // Menor o igual
document.writeln('<br>'); // Salto de línea